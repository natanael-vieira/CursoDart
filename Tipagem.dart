  var exemp1 = 1; // Aqui a variável foi atribuída como int
  var exemp2 = 1.158; // Aqui a variável foi atribuída como double
  var exemp3 = "Seu texto aqui."; // Aqui a variável foi atribuída como String
  var isTrue = "exemp4"; // Aqui a variável assumiu um bool

  // Porém podemos colocar o tipo que a variável vai assumir na atribuição da variável

  int num1 = 1;
  double num2 = 1.71;
  String text1 = "Seu texto aqui!";
  bool isTrue = true;
  
}
